#include <bits/stdc++.h>
using namespace std;
struct Rect {
  long long x1, y1, x2, y2, t;
};

int main() {
  int n, t;
  cin >> n >> t;

  vector<Rect> rects(n);
  vector<long long> x;
  vector<long long> y;

  for (int i = 0; i < n; i++) {
    cin >> rects[i].x1 >> rects[i].y1 >> rects[i].x2 >> rects[i].y2 >>
        rects[i].t;
    x.push_back(rects[i].x1);
    x.push_back(rects[i].x2);
    y.push_back(rects[i].y1);
    y.push_back(rects[i].y2);
  }

  sort(x.begin(), x.end());
  sort(y.begin(), y.end());

  x.erase(unique(x.begin(), x.end()), x.end());
  y.erase(unique(y.begin(), y.end()), y.end());

  unordered_map<long long, int> mapX;
  unordered_map<long long, int> mapY;

  for (size_t i = 0; i < x.size(); i++) {
    mapX[x[i]] = i;
  }

  for (size_t i = 0; i < y.size(); i++) {
    mapY[y[i]] = i;
  }

  vector<vector<long long>> diff(y.size(), vector<long long>(x.size(), 0));

  for (Rect rect : rects) {
    int x1Idx = mapX[rect.x1];
    int x2Idx = mapX[rect.x2];
    int y1Idx = mapY[rect.y1];
    int y2Idx = mapY[rect.y2];

    for (long long y = y1Idx; y < y2Idx; y++) {
      diff[y][x1Idx] += rect.t;
      diff[y][x2Idx] -= rect.t;
    }
  }
 
  for (size_t i = 0; i < y.size(); i++) {
    long long num = 0;
    for (size_t j = 0; j < x.size(); j++) {
      diff[i][j] += num;
      num = diff[i][j];
    }
  }

  long long area = 0;
  for (size_t i = 0; i < y.size() - 1; i++) {
    for (size_t j = 0; j < x.size() - 1; j++) {
      if (diff[i][j] >= t) {
        area += (x[j + 1] - x[j]) * (y[i + 1] - y[i]);
      }
    }
  }

  cout << area;
}